// ── LLM-powered gameplay script generation ─────────────────────────────────

async function generateGameplayScript(target: ExportTarget, p: {
  title: string;
  genre: string;
  artStyle: string;
  protagonist: Record<string, unknown>;
  storyData: Record<string, unknown>;
  combatData: Record<string, unknown>;
}): Promise<string | null> {
  const languageMap: Partial<Record<ExportTarget, string>> = {
    godot:     "GDScript 4.x (Godot 4)",
    unity:     "C# (Unity 2022 LTS)",
    gamemaker: "GML (GameMaker Language Studio 2)",
    construct: "JSON event sheet format",
    unreal:    "C++ for Unreal Engine 5",
    rpgmaker:  "JavaScript (RPG Maker MZ plugin)",
    phaser:    "JavaScript (Phaser 3)",
  };
  const lang = languageMap[target];
  if (!lang) return null;

  const systemPrompt = `You are an expert game developer who writes clean, well-commented ${lang} code.
Generate a focused, production-ready gameplay script for the game described below.
Output ONLY the code — no explanations, no markdown code fences.`;

  const userPrompt = `Game: ${p.title}
Genre: ${p.genre}
Art Style: ${p.artStyle}
Protagonist: ${p.protagonist.name ?? "Hero"} — ${p.protagonist.backstory ?? ""}
Core Loop: ${p.storyData.coreLoop ?? ""}
Combat: ${p.combatData.combatSystem ?? ""}

Write a ${lang} script that implements:
1. Player state machine (idle, walk, run, attack, hurt, dead)
2. Basic combat: melee attack, health system, damage feedback
3. Save/load helper that persists player progress
Keep it under 150 lines, modular, and well commented.`;

  try {
    const result = await routeTask("coding", [
      { role: "system", content: systemPrompt },
      { role: "user",   content: userPrompt },
    ]);
    return result.content ?? null;
  } catch {
    return null;
  }
}

