router.get("/projects/:id/export", requireAuth, async (req, res) => {
  const projectId = req.params["id"] as string;
  const ownerId = req.user!.sub;
  const target = ((req.query["target"] as string) || "godot") as ExportTarget;

  const [project] = await db
    .select()
    .from(projects)
    .where(and(eq(projects.id, projectId), eq(projects.ownerId, ownerId)))
    .limit(1);

  if (!project) {
    res.status(404).json({ error: "Project not found" });
    return;
  }

  const projectAssets = await db
    .select()
    .from(assets)
    .where(and(eq(assets.projectId, projectId), eq(assets.ownerId, ownerId)));

  const slug = project.title.toLowerCase().replace(/[^a-z0-9]+/g, "-").slice(0, 40);
  const filename = `${slug}-${target}-export.zip`;

  try {
    const zip = new JSZip();

    // ── game-data.json ─────────────────────────────────────────────────────
    const gameData = {
      title: project.title,
      description: project.description,
      genre: project.genre,
      artStyle: project.artStyle,
      platform: project.platform,
      progress: project.progress,
      generatedAt: project.lastGeneratedAt?.toISOString() ?? new Date().toISOString(),
      exportTarget: target,
      exportedBy: "GenForgeAI v1.0",
      story: project.storyData,
      world: project.worldData,
      characters: project.characterData,
      combat: project.combatData,
      assetManifest: project.assetManifest,
    };
    zip.file("game-data.json", JSON.stringify(gameData, null, 2));

    // ── README ─────────────────────────────────────────────────────────────
    zip.file(
      "README.md",
      buildReadme(project.title, target, {
        genre: project.genre,
        artStyle: project.artStyle,
        description: project.description,
        worldData: (project.worldData ?? {}) as Record<string, unknown>,
        storyData: (project.storyData ?? {}) as Record<string, unknown>,
        characterData: (project.characterData ?? {}) as Record<string, unknown>,
        combatData: (project.combatData ?? {}) as Record<string, unknown>,
      })
    );

    // ── Target-specific scaffolding ────────────────────────────────────────
    const cd = (project.characterData ?? {}) as Record<string, unknown>;
    const sd = (project.storyData     ?? {}) as Record<string, unknown>;
    const comd = (project.combatData  ?? {}) as Record<string, unknown>;
    const protagonist = (cd.protagonist as Record<string, unknown>) ?? {};
    const protagonistName = String(protagonist.name ?? "Hero");

    // Kick off LLM code gen in parallel with ZIP assembly (best-effort)
    const llmCodePromise = generateGameplayScript(target, {
      title:     project.title,
      genre:     project.genre,
      artStyle:  project.artStyle,
      protagonist,
      storyData: sd,
      combatData: comd,
    });

    if (target === "godot") {
      zip.file("project.godot", godotProjectFile(project.title));
      zip.file("scenes/main.tscn", godotMainScene(project.title, protagonistName));
      zip.file("scripts/.gitkeep", "");
    } else if (target === "phaser") {
      zip.file("index.html", phaserIndexHtml(project.title, project.genre));
      zip.file("src/.gitkeep", "");
    } else if (target === "unity") {
      zip.file("Scripts/GameData.cs", unityGameDataCs(project.title, project.genre, project.artStyle));
      zip.file("Assets/.gitkeep", "");
    } else if (target === "gamemaker") {
      zip.file("project.yyp", gameMakerYYP(project.title));
      zip.file("objects/Player/Create_0.gml", gameMakerPlayerCreate(protagonistName, project.genre));
      zip.file("objects/Player/Step_0.gml",   gameMakerPlayerStep(project.genre));
      zip.file("sprites/.gitkeep", "");
      zip.file("sounds/.gitkeep", "");
    } else if (target === "construct") {
      zip.file("project.c3proj",  constructProject(project.title, project.genre));
      zip.file("game-scene.json", constructEventSheet(project.title));
      zip.file("images/.gitkeep", "");
    } else if (target === "unreal") {
      const uprojectName = project.title.replace(/[^a-zA-Z0-9]/g, "");
      zip.file(`${uprojectName}.uproject`, unrealUProject(project.title));
      zip.file(`Source/${uprojectName}GameData.h`, unrealGameDataHeader(project.title, project.genre, project.artStyle));
      zip.file("Content/.gitkeep", "");
    } else if (target === "rpgmaker") {
      zip.file("js/plugins/GenForgePlugin.js", rpgMakerPlugin(project.title, project.genre, {
        protagonist,
        storyData: sd,
        combatData: comd,
      }));
      zip.file("img/.gitkeep", "");
    }

    // Attach LLM-generated gameplay script (if it arrived in time)
    const llmCode = await llmCodePromise;
    if (llmCode) {
      const ext: Partial<Record<ExportTarget, string>> = {
        godot: "scripts/Player.gd", phaser: "src/Player.js", unity: "Scripts/PlayerController.cs",
        gamemaker: "objects/Player/LLM_Gameplay.gml", construct: "llm-events.json",
        unreal: "Source/PlayerCharacter.cpp", rpgmaker: "js/plugins/GenForgeGameplay.js",
      };
      const path = ext[target];
      if (path) zip.file(path, llmCode);
    }

    // ── Generated images ───────────────────────────────────────────────────
    for (const asset of projectAssets) {
      if (!asset.url) continue;
      const commaIdx = asset.url.indexOf(",");
      if (commaIdx === -1) continue;
      try {
        const base64 = asset.url.slice(commaIdx + 1);
        const safeName = (asset.name ?? "asset")
          .toLowerCase()
          .replace(/[^a-z0-9]+/g, "-")
          .slice(0, 50);
        const ext = (asset.mimeType ?? "image/png").split("/")[1] ?? "png";
        zip.file(`assets/${asset.category ?? "misc"}/${safeName}.${ext}`, base64, {
          base64: true,
        });
      } catch {
        // skip malformed
      }
    }

    const buf = await zip.generateAsync({ type: "nodebuffer", compression: "DEFLATE" });

    res.setHeader("Content-Type", "application/zip");
    res.setHeader("Content-Disposition", `attachment; filename="${filename}"`);
    res.setHeader("Content-Length", String(buf.length));
    res.send(buf);
  } catch (err) {
    req.log.error({ err }, "Export failed");
    if (!res.headersSent) {
      res.status(500).json({ error: "Export generation failed" });
    }
  }
});

