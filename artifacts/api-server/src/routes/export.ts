function gameMakerPlayerStep(genre: string): string {
  return `/// @description Player — Step event
/// Generated by GenForgeAI for ${genre}

// Gather input
inputLeft  = keyboard_check(vk_left)  || keyboard_check(ord("A"));
inputRight = keyboard_check(vk_right) || keyboard_check(ord("D"));
inputUp    = keyboard_check(vk_up)    || keyboard_check(ord("W"));
inputDown  = keyboard_check(vk_down)  || keyboard_check(ord("S"));
inputAtk   = keyboard_check_pressed(ord("Z")) || keyboard_check_pressed(vk_space);

// Movement
var _dx = (inputRight - inputLeft) * speed;
var _dy = (inputDown  - inputUp)   * speed;
if (place_meeting(x + _dx, y, obj_Solid)) _dx = 0;
if (place_meeting(x, y + _dy, obj_Solid)) _dy = 0;
x += _dx;
y += _dy;

// Attack
if (inputAtk && state != "attack") {
    state = "attack";
    alarm[0] = 20; // reset state after 20 steps
}
`;
}

